using System;
using System.Collections.Generic;
using System.Text;

namespace Operations
{
    public class MathOperations
    {
        public int Add(int num1, int num2)
        {
            return num1 + num2;
        }
        public double Add(double double1, double double2, double double3)
        {
            return double1 + double2 + double3;
        }
        public decimal Add(decimal decimal1, decimal decimal2, decimal decimal3)
        {
            return decimal1 + decimal2 + decimal3;
        }
    }
}
